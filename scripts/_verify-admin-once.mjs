import { chromium } from "playwright";
const browser = await chromium.launch({ headless: true, channel: "chrome" });
const context = await browser.newContext();
await context.setExtraHTTPHeaders({ "x-bb-admin-ui-probe": "1" });
const page = await context.newPage();
await page.goto("http://127.0.0.1:3000/admin", { waitUntil: "networkidle", timeout: 90000 });
const url = page.url();
const body = await page.locator("body").innerText();
const OLD = ["審査待ち案件","公開中案件","交渉数","成約件数","成約金額合計","手数料合計"];
const NEW = ["商品","交渉","手数料","売上","審査待ち商品","表示区分：商品 / 交渉 / 手数料 / 売上"];
console.log("URL", url);
console.log("OLD hits", OLD.filter(s => body.includes(s)));
console.log("NEW hits", NEW.filter(s => body.includes(s)));
await page.screenshot({ path: "C:/Users/shuro/Desktop/BrandBridge/.tmp/admin-final.png", fullPage: true });
await browser.close();
if (!url.includes("/admin") || url.includes("/dashboard")) process.exit(2);
if (OLD.some(s => body.includes(s))) process.exit(3);
if (!body.includes("審査待ち商品") || !body.includes("売上")) process.exit(4);
console.log("OK");
